    private void RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterActionPerformed
        try{
            conn=Test.ConnectrDb();
            String query = "insert into admin(Serial,Initial,Username,Password)"+"values(?,?,?,?)";
            PreparedStatement pst = conn.prepareStatement(query);
            
            if(Serial.getText().equals("")){
                pst.setString(1, null);
            }else{
                pst = conn.prepareStatement(query);
                pst.setString(1, Serial.getText());
            }
            pst.setString(2, GetInitial.getText());
            pst.setString(3, GetName.getText());
            pst.setString(4, GetPassword.getText());
            pst.execute();
            pst.close();
            JOptionPane.showMessageDialog(null, "Signup Succesfull!");
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_RegisterActionPerformed

