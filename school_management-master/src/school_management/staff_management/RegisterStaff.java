    public void setData(int staffId,ManageStaff mg){
        
        btnUpdate.setVisible(true);
        btnAdd.setVisible(false);
        lblStaffId.setVisible(true);
        lblStaff.setVisible(true);
        this.mg=mg;
        SimpleDateFormat df = new SimpleDateFormat("EEEE dd/MM/yyyy");
        staffModel st=new staffModel();
        try{
            ResultSet rs=st.selectStaff(staffId);
            
            
        
            while(rs.next()){
                Date dobDate = df.parse(df.format(rs.getDate("dateOfBirth")));
                Date joinDate = df.parse(df.format(rs.getDate("joinedDate")));
                lblStaff.setText(Integer.toString(rs.getInt("staff_Id")));
                txtFirstName.setText(rs.getString("firstName"));
                txtLastName.setText(rs.getString("lastName"));
                cmbGender.setSelectedItem(rs.getString("gender"));
                dateDob.setDate(dobDate);
                cmbReligion.setSelectedItem(rs.getString("religion"));
                txtNIC.setText(rs.getString("NIC"));
                cmbMarStatus.setSelectedItem(rs.getString("MaritialStatus"));
                txtPhone.setText(rs.getString("phone"));
                txtMobile.setText(rs.getString("mobile"));
                txtMail.setText(rs.getString("email"));
                txtCurAdd.setText(rs.getString("currentAdd"));
                txtPerAdd.setText(rs.getString("permanantAdd"));
                txtEducational.setText(rs.getString("educationQual"));
                txtProff.setText(rs.getString("proffQual"));
                dateJoined.setDate(joinDate);
                txtSalary.setText(Float.toString(rs.getFloat("salary")));
                txtBank.setText(rs.getString("bank"));
                txtAccntNo.setText(rs.getString("accntNo"));
                                                               
            }
        } catch (SQLException ex) {
            Logger.getLogger(RegisterStaff.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(RegisterStaff.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (Exception ex) {
            Logger.getLogger(RegisterStaff.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
