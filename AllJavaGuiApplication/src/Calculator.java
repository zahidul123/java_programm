    private void jbtneqlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtneqlActionPerformed
        // TODO add your handling code here:
        secondnum=Double.parseDouble(caldisplay.getText());
        String disfrom;
        switch(operation){
            case "+":
                result=firstnum+secondnum;
                disfrom=String.format("%.2f",result);
                caldisplay.setText(disfrom);
                break;
            case "-":
                result=firstnum-secondnum;
                disfrom=String.format("%.2f",result);
                caldisplay.setText(disfrom);
                break;
            case "*":
                result=firstnum*secondnum;
                disfrom=String.format("%.2f",result);
                caldisplay.setText(disfrom);
                break;
            case "/":
                result=firstnum/secondnum;
                disfrom=String.format("%.2f",result);
                caldisplay.setText(disfrom);
                break;
            default:
               caldisplay.setText(""); 
                
        }
    }//GEN-LAST:event_jbtneqlActionPerformed

